		        <h2>Circuit : La Basse Alsace (D&eacute;part 15h)</h2>
	          <div>
                <p><br />
                    D&eacute;part : 15h (Strasbourg-gare)<br />
Retour : 22h <br />
D&eacute;part garanti &agrave; partir de 4 personnes <br />
Dur&eacute;e du circuit : 7h <br />
Prix:<br />
&bull;	Circuit-conduit (transport) : &agrave; partir de 39&euro;<br />
&bull; Prestations et repas : 31.40&euro; <br />
<br />
<em>Mettez la main &agrave; la p&acirc;te en pr&eacute;parant vous-m&ecirc;me votre repas dans une  table d&rsquo;h&ocirc;te, puis d&eacute;couvrez la poterie artisanale alsacienne, ainsi  que le dernier apiculteur professionnel de Strasbourg&hellip;</em> <br />
<br />
<strong><u>Etape 1: Visite de la brasserie artisanale Uberach </u></strong><br />
<br />
Pour  d&eacute;buter ce parcours, nous nous rendrons &agrave; la <strong><a target="_blank" href="http://www.biere-france.com/index.php?page=detail&amp;Id_brasserie=46" title="brasserie">brasserie de Uberach</a></strong>&nbsp;  pour une visite/d&eacute;gustation (avec bretzel frais) de la brasserie. Cette  brasserie fabrique aujourd&rsquo;hui six bi&egrave;res diff&eacute;rentes, peu houblonn&eacute;es,  &eacute;labor&eacute;es avec de l'eau de source, des <a target="_blank" href="http://fr.wikipedia.org/wiki/Houblon" title="houblons"><strong>houblons</strong></a> alsaciens et des malts s&eacute;lectionn&eacute;s ; elles sont ambr&eacute;es ou brunes, non  filtr&eacute;es, non pasteuris&eacute;es; elles contiennent des saveurs de fleurs, de  fruits, de plantes aromatiques naturelles. Une v&eacute;ritable fiert&eacute;  r&eacute;gionale! <br />
<br />
<strong><u>Etape 2: Visite de la ferme bio Adam</u></strong><br />
<br />
Deuxi&egrave;me &eacute;tape: la visite de la ferme ADAM de Wahlenheim. Cette ferme  typique alsacienne vous fera d&eacute;couvrir son exploitation ainsi que la  fabrication de produits laitiers bio.Vous aurez ainsi l'occasion de  go&ucirc;tez &agrave; d&rsquo;authentiques produits fermiers!<br />
<br />
<strong><u>Etape 3:   Balade en cal&egrave;che dans un village typique</u></strong> <br />
<br />
Pour finir l'apr&egrave;s-midi, laissez-vous s&eacute;duire par une petite visite du  village en cal&egrave;che. D&eacute;couvrez de superbes demeures alsaciennes  typiques, aux colombages anciens, ainsi qu&rsquo;une ballade dans le Ried  pour appr&eacute;cier la faune et la flore des mar&eacute;cages ass&eacute;ch&eacute;s de la plaine  du Rhin.<br />
<br />
<strong><u>Etape 4 : D&icirc;ner &agrave; la Table d'h&ocirc;te, chez Mr et Mme Lapp</u></strong><br />
<br />
Place &agrave; un savoureux d&icirc;ner &agrave;  la <a target="_blank" href="http://gite67.lapp.free.fr/n5233_vert_ecrit_noir.htm" title="table d'hote"><strong>table  d&rsquo;h&ocirc;te</strong></a> &agrave; Kurtzenhouse am&eacute;nag&eacute;e dans un corps de ferme situ&eacute;e au c&oelig;ur du  village. Ici sont pr&eacute;par&eacute;es avec savoir-faire et tradition toutes  sortes de d&eacute;licieuses sp&eacute;cialit&eacute;s alsaciennes tels que : le <a target="_blank" href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-baeckehoffe.php" title="baeckofe"><strong>Baeckeofe</strong></a>, la <a target="_blank" href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-tarte-flambee.php"><strong>tarte flamb&eacute;e</strong></a>, le <a target="_blank" href="http://www.alsace-route-des-vins.com/NewVersion/index.cfm/fuseaction/Gastronomie.ShowRecette/ID/19/Language/Fr.cfm"><strong>coq au Riesling</strong></a>, le repas marcaire et la <strong><a target="_blank" href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-choucroute.php">Choucroute</a> </strong>que nous d&eacute;gusterons dans ce cadre de charme.                </p>
