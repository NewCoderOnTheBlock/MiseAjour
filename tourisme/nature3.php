                    <strong><u><br />
                    Etape 2: Visite de la cave Mosbach &agrave;  Marlenheim</u></strong><br />
                    <br />
Pendant  le temps de cuisson nous visiterons la <strong><a target="_blank"  href="http://www.vinsmosbach.com/index.php?lg=fr" title="cave">cave Mosbach</a></strong> &agrave; Marlenheim,  cit&eacute; viticole &agrave; la porte de la  <strong><a target="_blank"  href="http://www.laroutedesvins.net/" title="route des vins">route des vins d'Alsace</a></strong>&raquo;,  &agrave; mi-distance entre Strasbourg et Saverne. C&rsquo;est une cave qui a une  capacit&eacute; d'environ 3500 hectolitres, et qui est constitu&eacute;e  principalement de foudres de ch&ecirc;ne dont la majorit&eacute; a une centaine  d'ann&eacute;es.<br /><br />
                    <strong><u><br />
                    Etape 3: Retour &agrave;  Wintzenheim-Kochersberg pour d&eacute;guster le plat </u></strong><br />
                    <br />
Retour  &agrave; la <strong><a target="_blank"  href="http://www.kochersberg.fr/tourisme_liste_chambres_et_gites_ruraux.htm" title="table d'hote">table  d&rsquo;h&ocirc;te</a></strong>  de Wintzenheim-Kochersberg,pour une petite balade dans le  village &agrave; cheval.<br />
Ensuite,  nous passerons &agrave; table pour d&eacute;guster notre sp&eacute;cialit&eacute; alsacienne.<br /><br />
                    <strong><u><br />
                    Etape 4: D&eacute;couverte de la ferme du Gaveur &agrave; Woellenheim</u></strong> :<br />
                    <br />
Ensuite nous visiterons la ferme les  <a target="_blank"  href="http://www.kochersberg.fr/ferme_gaveur_kochersberg.htm" title="gaveurs du kochersberg"><strong>Gaveurs du Kochersberg</strong></a>, producteur de <a target="_blank"  href="http://fr.wikipedia.org/wiki/Foie_gras"><strong>fois Gras</strong></a>.  Ferme typiquement alsacienne, &agrave; pans de bois, autour d&rsquo;une grande cour.  Derri&egrave;re les b&acirc;timents, les canards sont &eacute;lev&eacute;s en plein champ : les  petits d&rsquo;un c&ocirc;t&eacute;, les plus grands de l&rsquo;autre, sous la garde de quelques  oies qui cacardent gaiement. Ce domaine produit &eacute;galement de l&rsquo;asperge,  de l&rsquo;ail, de l&rsquo;oignon, de l&rsquo;&eacute;chalote et de la betterave sucri&egrave;re.</p>
