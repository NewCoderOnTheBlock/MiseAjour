					<p align="justify"><strong><u> Etape 3: Maison du pain d’épice à Gertwiller 


</u></strong>:<br />
</p>
		            <p><strong>Le mus&eacute;e du Pain d&rsquo;&eacute;pices&nbsp;: des saveurs authentiques</strong></p>
              <p align="justify">Gertwiller, charmant petit village viticole, est situ&eacute; au  pied du <a target="_blank"  href="javascript:void(0)">Mont Sainte Odile</a>, entre plaine et  collines viticoles.<br />
                        Le savoir-faire et la  concentration des p&acirc;tissiers dans cette localit&eacute; ont contribu&eacute; &agrave; faire du <a target="_blank"  href="http://www.pays-de-barr.com/40frc10-Produits-locaux.htm">pain d'&eacute;pices</a> une sp&eacute;cialit&eacute; culturelle et gastronomique d'Alsace (voir <a target="_blank"  href="javascript:void(0)">Mus&eacute;e du Pain d'Epices</a>). Deux entreprises locales  produisent ces douceurs. <br />
                  Le Mus&eacute;e du Pain d&rsquo;&eacute;pices et de  l'Art Populaire Alsacien que nous visiterons s&rsquo;&eacute;tend sur 180m2 dans le grenier  de l&rsquo;ancienne grange d&icirc;mi&egrave;re de Gertwiller, o&ugrave; sont expos&eacute;s quelque 7000 pi&egrave;ces  relatives au pain, au pain d &eacute;pices, &agrave; la biscuiterie, &agrave; la chocolaterie, &agrave;  l&rsquo;art populaire alsacien et &agrave; la vie rurale d autrefois. <em><strong><br />
                  Dur&eacute;e:1h<br />
              Prix: 2.50&euro;</strong> </em></p>
		            <p><strong><u>Etape 4: Domaine vinicole Klipfel à Barr </u></strong></p>
		            <p align="justify">Le domaine <a href="http://www.klipfel.com/" target="_blank">KLIPFEL</a>&nbsp;: une cave de renom <br />
Barr est capitale viticole du Bas-Rhin. Sa  vigne grimpe sur les c&ocirc;teaux qui bordent la vall&eacute;e de la Kirneck, signal&eacute;e par la  petite maison blanche haut perch&eacute;e. On y retrouve le c&eacute;l&egrave;bre Grand Cru du <a target="_blank"  href="http://www.pays-de-barr.com/37frc2-Grands-Crus.htm#Kirchberg">Kirchberg</a>. <br />
Au Domaine KLIPFEL vous pourrez visiter  l'une des plus grandes caves d'Alsace en foudres de ch&ecirc;ne dont certains  contiennent plus de 20.000   litres. Leurs principaux c&eacute;pages sont Gewurztraminer, Riesling, Tokay Pinot-Gris.<br />
La famille KLIPFEL a eu pour habitude de rechercher la surmaturation et  la pourriture noble (Vendanges Tardives) dans les grandes ann&eacute;es. Ainsi,  l'oenoth&egrave;que KLIPFEL contient des vins des ann&eacute;es 1919, 1921, 1929, 1937, 1939,  1941, 1942, 1943, 1945, 1959, etc &hellip;. Alors que les vins Vendanges Tardives et  S&eacute;lection de Grains Nobles ont &eacute;t&eacute; officiellement agr&eacute;&eacute;s en 1984.<br />
La Maison KLIPFEL est l'une des toutes premi&egrave;res en 1984, &agrave; avoir &eacute;lev&eacute;  son Pinot Noir en barriques bourguignonnes, le fameux Rouge de Barr. </p>

<p><strong><u>Etape 5: Abbatiale d’Ebersmunster </u></strong></p>
		            <p align="justify">Au cœur d’un village typique du Ried, dressant fièrement ses trois tours, l’<a href="http://www.alsace-visite-guidee.info/ebersmunster.htm" target="_blank">abbatiale Saint Maurice d’Ebersmunster</a> est sans aucun doute le monument baroque le plus spectaculaire d’Alsace,et même de tout l’Est de la France. Bâtie par l'architecte autrichien Peter Thumb, celle-ci abrite un intérieur somptueux et particulièrement lumineux et coloré, recelant de chefs-d’œuvres de décoration et d’ornement. Par ailleurs, vous découvrirez un orgue réalisé par André Silbermann superbement restauré, un magnifique maître-autel, ainsi que de magnifiques peintures.
Vous serez émerveillé par ce joyau d’art barque alémanique, classée monument historique.
Un monument qu’on doit absolument faire figurer sur son itinéraire alsacien !
