</u></strong>:
			  </p>
					<p align="justify">La famille DOCK vous accueille chaleureusement dans leur domaine à Heilingenstein pour vous faire visiter leur cave et déguster tout d’abord le célèbre « <a href="http://www.sdv.fr/pages/andrieu/" target="_blank">Klevener</a> », uniquement produit sur les terres du village, mais aussi d’autres vins fins de leur production…</p>
					
			  <p align="left">
				      <br />
                      <strong><u>Etape 2: Pause repas à l’<a href="http://www.auberge-du-maennelstein.com/" target="_blank">Auberge du Maennelstein</a>

