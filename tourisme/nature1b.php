				<i>Mettez la main à la pâte en préparant vous-même votre repas dans une table d’hôte, puis découvrez la poterie artisanale alsacienne, ainsi que le dernier apiculteur professionnel de Strasbourg…</i>
				<br />
				<br />
				<strong><u>Etape 1: Visite de la brasserie artisanale Uberach				</u></strong><br />
				  <br />
Pour  d&eacute;buter ce parcours, nous nous rendrons &agrave; <a href="http://www.biere-france.com/index.php?page=detail&amp;Id_brasserie=46" title="brasserie"><strong>la  brasserie de Uberach</strong></a>&nbsp;  pour une visite/d&eacute;gustation (avec bretzel frais) de la brasserie. Cette  brasserie fabrique aujourd&rsquo;hui six bi&egrave;res diff&eacute;rentes, peu houblonn&eacute;es,  &eacute;labor&eacute;es avec de l'eau de source, <a href="http://fr.wikipedia.org/wiki/Houblon" title="houblons"><strong>des  houblons</strong></a> alsaciens et des malts s&eacute;lectionn&eacute;s ; elles sont ambr&eacute;es ou brunes, non  filtr&eacute;es, non pasteuris&eacute;es; elles contiennent des saveurs de fleurs, de  fruits, de plantes aromatiques naturelles. Une v&eacute;ritable fiert&eacute; r&eacute;gionale! <br />
				<br />
                    <strong><u>Etape 2: Visite de la ferme bio Adam</u></strong><br />
                    <br />
Deuxi&egrave;me &eacute;tape: la visite de la ferme ADAM de Wahlenheim. Cette ferme typique alsacienne vous fera découvrir son exploitation ainsi que la fabrication de produits laitiers bio.Vous aurez ainsi l'occasion de goûtez à d’authentiques produits fermiers!<br />
<br />
<strong><u>Etape 3: 
Balade en cal&egrave;che dans un village typique</u></strong> <br />
				  <br />
				  Pour finir l'apr&egrave;s-midi, laissez-vous s&eacute;duire par une petite visite du village en cal&egrave;che. D&eacute;couvrez de superbes demeures alsaciennes typiques, aux colombages  anciens, ainsi qu&rsquo;une ballade dans le Ried pour appr&eacute;cier la faune  et la flore des mar&eacute;cages ass&eacute;ch&eacute;s de la plaine du Rhin.<br />
				  <br />
				  <strong><u>Etape 4 : D&icirc;ner &agrave; la Table d'h&ocirc;te, chez Mr et Mme Lapp</u></strong><br /> 
				  <br />
			  Place &agrave; un savoureux d&icirc;ner &agrave;  la <a href="http://gite67.lapp.free.fr/n5233_vert_ecrit_noir.htm" title="table d'hote"><strong>table  d&rsquo;h&ocirc;te &agrave; Kurtzenhouse</strong></a> am&eacute;nag&eacute;e dans un corps de ferme situ&eacute;e au c&oelig;ur du village. Ici sont pr&eacute;par&eacute;es avec savoir-faire et tradition toutes sortes de d&eacute;licieuses sp&eacute;cialit&eacute;s  alsaciennes tels que : <a href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-baeckehoffe.php" title="baeckofe"><strong>le  Baeckeofe</strong></a>, <a href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-tarte-flambee.php"><strong>la  tarte flamb&eacute;e</strong></a>, <a href="http://www.alsace-route-des-vins.com/NewVersion/index.cfm/fuseaction/Gastronomie.ShowRecette/ID/19/Language/Fr.cfm"><strong>le  coq au Riesling</strong></a>, le repas marcaire et la <strong><a href="http://www.tourisme-alsace.com/fr/recettes-alsaciennes/recettes-alsaciennes-choucroute.php">Choucroute</a> </strong>que nous d&eacute;gusterons dans ce cadre de charme.</p>
